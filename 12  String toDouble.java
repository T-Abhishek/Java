// Java program to convert String to Double
// using parseDouble()

public class GFG {

	// main method
	public static void main(String args[])
	{

		// create a String
		String str = "2033.12244";

		// convert into Double
		double str1 = Double.parseDouble(str);

		// print String as Double
		System.out.println(str1);
	}
}
